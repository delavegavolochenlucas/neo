# Loop é uma forma de repetir aquelas linhas de código várias vezes, sem ter que escrever tudo de novo.

for i in range(1, 11):
    print(i)

# o for pega cada número do range, e coloca na variável escolhida (i nesse caso), e a cada loop ele é atualizado com o próximo número do range, e o print serve para mostrar cada número do loop no terminal.