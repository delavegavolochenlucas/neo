# Dicionários servem para armazenar dados em pares de chave e valor, e são definidos com chaves {}.

pessoa = {
    "nome": "Lucas",
    "idade": 14,
    "cidade": "Curitiba",
    "altura": 1.65,
    "sonho": "Ser o maior empreendedor do planeta"
}

print(pessoa["nome"])
print(pessoa["idade"])
print(pessoa["cidade"])
print(pessoa["altura"])
print(pessoa["sonho"]) 

# nesse código, criamos um dicionário chamado pessoa, que contém cinco pares de chave e valor (nome: Lucas, idade: 14, cidade: Curitiba, altura: 1.65 e sonho: Ser o maior empreendedor do planeta), e utilizamos o print para mostrar cada valor do dicionário no terminal, utilizando a chave correspondente para acessar o valor. O print(pessoa["nome"]) serve para mostrar o valor associado à chave "nome", que é "Lucas", e assim por diante para as outras chaves.