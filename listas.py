# Listas são usadas para armazenar múltiplos valores em uma única variável, e são definidas com colchetes [].

frutas = ["maça", "banana", "laranja", "uva", "morango"]
for fruta in frutas:
    print(fruta)

# nesse código, criamos uma lista chamada frutas, que contém cinco valores (maça, banana, laranja, uva e morango), e utilizamos um loop for para percorrer cada elemento da lista, e o print serve para mostrar cada fruta no terminal. O for pega cada valor da lista frutas, e coloca na variável escolhida (fruta nesse caso), e a cada loop ele é atualizado com o próximo valor da lista.