# Condicionais é uma forma de executar algo se basendo em sua condição, ou seja, se algo for verdadeiro ou falso.

idade = 10

if idade >= 18:
    print("voce é adulto")

elif idade <= 13:
    print("voce é criança")

else:
    print("voce é adolescente")

# if é para dizer que se a condição for verdadeira, execute algo.
# elif é para dizer que se a condição for falsa, mas outra condição for verdadeira, execute algo.
# else é para dizer que se todas as condições anteriores forem falsas, execute algo.